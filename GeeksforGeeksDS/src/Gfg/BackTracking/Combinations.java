package Gfg.BackTracking;

import java.util.Arrays;

public class Combinations {


    public static void main(String[] args) {
        int n = 4 ;

        int[] array = new int[n];
        for(int i=0;i<n;i++){
            array[i] = i+1;
        }

        System.out.println(Arrays.toString(array));


    }




}
